									<span style="font-size: 20px; font-weight: bold;">香港合唱新聲</span><p>洪思行<br>2013 年 10 月 26 日</p>
									<p style="text-align: justify;">									
									在八月初，筆者一星期內聽了兩場大型合唱音樂會，兩個表演團體也是以學生為骨幹的合唱團，表演曲目不約而同都是德國作品，它們分別是「生‧息」音樂會及聲蜚合唱節的揭幕音樂會。<br><br>
									先說「生‧息」音樂會，它由成立3年多的 Ponte Singers 舉辦，指揮是林屴汧，並由 Ponte Orchestra 負責伴奏。這場音樂會的「主菜」是奧爾夫的《布蘭詩歌》。為演唱這套氣勢磅礡的樂曲，除了擴大合唱團外， Ponte Singers 還請來其他合唱團如香港新青年合唱團與滬江小學合唱團助陣，合唱團人數高達194人，連同樂團、獨唱和指揮，演出人數接近三百人，把香港大會堂音樂廳的舞 台「逼爆」！<br><br>
									以這個規模來演唱著名的〈噢！命運〉，聲勢浩大，震攝力驚人。雖然 Ponte Singers 的成員多來自不同中學和大學，而且是次有很多新成員加入，但他們的聲音已能融和在一起，而且各聲部的聲量大致平均，完全不像是一個新近湊合而成的合唱團。<br><br>
									從旋律、和聲和結構來說，《布蘭詩歌》的難度不是非常高：旋律算是易上口，沒有複雜的和聲，也沒有巴洛克時期作品的複音織體，主要以齊唱為主。但要以拉丁 文、德文和法文演唱這套歌曲，演出經驗尚淺的學生不易掌握。話雖如此， Ponte Singers 的團員咬字不差，特別德文的子音演唱也清楚，相信他們下了很大的工夫。只是在部份急促的樂曲如〈當我們在酒館時〉便難免出現失誤。<br><br>
									演唱這套作品的另一個難處是它的長度，《布蘭詩歌》長約一小時，不但講求歌手們聲音的耐力，亦考驗他們的體力（要站上一小時）。然而當合唱團唱到最後一首歌曲，也就是重覆第一首歌曲〈噢！命運〉時聲威依舊，也沒有用上「死力」來唱，可見他們的耐力不成問題。<br><br>
									三名獨唱鍾嘉欣、吳智誠和胡永正皆有高水準演出，特別是胡永正，他演唱的歌曲最多，而且歌曲風格各有不同，時而要用真聲唱（對男中音來說）極高的音、時而 要用真聲混合假聲的方式唱歌、時而唱敘事性強的歌曲。他的咬字非常清晰，演唱充滿感情，語調變化多端，即使是不懂歌曲意思的觀眾，也能聽得出歌曲中表達的 情感。可惜胡永正在音量上有點遜色，特別是唱〈焚心以火〉中的高音時，歌聲都給樂團蓋過了。<br><br>
									童聲合唱團的一眾小朋友也絕對值得稱讚，不只是因為他們演唱得不俗，又能背譜演出，更難得是他們能夠乖乖地安坐在二樓，直至《布蘭詩歌》唱了超過一半才站起來唱歌，這份專注力對小學生來說實屬難得，連林屴汧在演唱完畢後也要特別多謝他們的付出。<br><br>
									</p>
									<h4>另一個層次的合唱表演</h4>
									<p style="text-align: justify;">
									至於聲蜚合唱節的揭幕音樂會，有別於上年，今屆合唱節舉辦了中學合唱領袖訓練營，先給予參與的學生密集式訓練，並改由香港小交響樂團伴奏。不變的是指揮還是由德來自德國的里霖指揮，演唱海頓的《創世紀》的德文版本。<br><br>
									這已是筆者第三次欣賞里霖的演出，每一次都不得不由衷敬佩這位年屆八十的老人家，因為在他的指揮棒下，合唱團不但有上佳的演出，更可說是提升至另一個層 次，脫胎換骨似的。當這個以中學生為主的合唱團演唱時，他們的聲音聽起來卻像是一個成人合唱團，並沒有一般學生合唱團那種還未完全成熟的聲音，各聲部的高 度融和更是不在話下。<br><br>
									里霖對咬字的要求非常高，因此聲蜚合唱節的合唱指導趙伯承曾在訪問中表示，在訓練營開始前，他已私下約了一些團員，為他們補習德語。若個別學員的進度落後 了，導師也會給他們額外練習，確保他們能跟得上。在導師和學生的共同努力下，觀眾可聽到他們的德語確是很精準，而且非常整齊，他們的努力都是值得的。<br><br>
									在里霖的調教下，四個聲部的表現平均，這不單是指聲量的平均，而是即使各聲部是在不同位置起奏，他們的力度與發音也相差無幾，不會有某一個聲部突出。此 外，里霖對部份樂曲的處理偏慢，相信是為了令合唱團和樂團能清晰地奏／唱出急奏的音，由此可見他追求的，是合唱團和樂團的整體性及音樂的清澈度。<br><br>
									另一點令筆者感到佩服的是里霖的指揮方法。雖然里霖年事已高，但他的記憶力超群，每次都是背譜演出，而且據悉在排練時他不需用樂譜，也能直接說出要從哪一 個小節開始。演出時觀眾可欣賞到里霖的兩套指揮模式：他會給合唱團「起手」的指示，讓團員有吸氣的時間，但給樂手的指示卻是直接指向他們。這不但是指揮技 巧，也反映出他頭腦清晰。里霖的指揮手法簡潔易明，例如他左手向右一掃，便能指示合唱團停止，然後順勢用右手向右邊的獨唱點一下，便能提示獨唱開始，整個 過程流暢非常，輕描淡寫，絲毫沒有多餘的動作。<br><br>
									負責獨唱的是葉葆菁、譚天樂和胡永正。雖然兩場音樂會只相隔一星期，但胡永正的表現沒有受密集的表演影響。他演唱宣敘調時發揮出色，由於宣敘調旋律性不 高，因此音樂的推進很依賴歌者的演繹。幸而胡永正的說唱充滿抑揚頓挫，令那些宣敘調聽來別有一番韻味。不過畢竟其他兩位獨唱都是歌劇歌手，加上他們是唱低 音的，胡永正跟他們合唱時聲音明顯較弱。葉葆菁的聲音嘹亮悅耳，當她唱一些戲劇性較強的歌曲時，會不自覺地用多了歌劇的唱腔。她還予人一種「越唱越有」的 感覺，聲音完全不見疲態，樂曲後段的高音依舊是高亢明亮。相比之下，譚天樂的耐力被比下去，當他唱到長樂曲的後段時，聲量不及開始時雄壯。<br><br>
									筆者認為，「生‧息」音樂會讓一群年青人展示他們對歌唱的熱愛，而聲蜚合唱節的揭幕音樂會就是借助大師級指揮，去打磨那些尚待琢磨的年青人，誘發他們的潛 能，兩個音樂會都給予了年輕人展示自己實力的機會。從結果來說，他們確是蠻有潛力，部份人在表演時更是面露微笑，全程投入，可看出他們是樂在其中。筆者相 信，只要他們能堅持合唱表演，他們必能成為香港合唱界將來的中流砥柱。<br><br>
									小記：大概是因為學生合唱團，這兩場音樂會都有很多家長來捧場，部份更明目張膽地拍照。在第一場音樂會中，職員在勸止一名觀眾不要拍照時，該名觀眾竟還在 豎拇指示意知道；在第二場音樂會中，表演前後都有觀眾不停用閃光燈拍照，對職員的勸止視若無睹。香港觀眾的質素如此，真是情何以堪。<br><br>
									</p>
									<hr>
