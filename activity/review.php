                                    <span style="font-size: 20px; font-weight: bold;">專訪 Ponte Singers 指揮林屴汧 - <br>當東方詩詞遇上西方音樂藝術</span><p>曾蓮</p><p>著名國際華文時報<br>2017 年 09 月 05 日</p>
                                    <p style="text-align: justify;">
                                        由香港青年音樂人組成，成立七年的香港合唱團Ponte Singers近日於香港理工大學賽馬會綜藝館舉辦了一場以中國詩詞為主題的合唱音樂會，以西方音樂藝術的形式，展現中國文學之美。樂團總指揮林屴汧接受本報記者專訪，探討東方詩詞在如今西方音樂教育為背景下如何傳承。<br><br>
                                        中文科對於大部份香港學生而言，是一門艱深的科目，且在流行文化大行其道的當代，少有人願意靜下心來閱讀承傳千年的傳統文學。不過，若一邊唱歌一邊默書，對於詩詞學習的動力就會大大改善。一群熱愛音樂的青年人，將古詩新詩配以優美的旋律，唱出中國文學之美。<br><br>
                                        Ponte Singers以音樂教育和感動聽眾為理念，每年舉辦主題式年度音樂會，繼2011年成功舉辦「聲•華」中國詩詞合唱音樂會以來，今年的音樂會再次以中國詩詞為主題，為觀眾獻上一場詩意盎然的演出。無論是氣勢磅礴的《大江東去》，還是溫柔婉約的《一棵開花的樹》，抑或是滄桑深遠的《橄欖樹》，耳熟能詳的詩歌在Ponte Singers的全新演繹下，別具一番風味。<br><br>
                                        樂團總指揮林屴汧分享，自小在合唱團訓練時就接觸到很多用中國古代詩詞或者民國時代用新詩作為歌詞的作品，覺得很受益，因此一直想辦一場以中國詩詞為主題的音樂會。如今夢想成真，也盼望未來能在整個華人地區推廣，將中國新古的詩詞全新演繹，令更多的年輕人感受詩詞之美，體驗中華文化的精深。<br><br>
                                        <b>中國詩詞源遠流長 音樂有助文學教育</b><br><br>
                                        林屴汧認為，每一個藝術的形式的存在都有其存在的價值，但是它在歷史的長河之內是否可以屹立住，這才是值得思考的，中國詩詞可以屹立三千幾年，相信一定有非常高的藝術價值。而且中國是世界上獨有的一個民族用圖形作為文字，古代文學從《詩經》開始，詩詞方面的造詣是比較高的。<br><br>
                                        當然現在世界日新月異，很多人都沒有時間和耐性去理解中國詩詞，它和現在追求的快節奏不同，它是一種「慢」的藝術。<br><br>
                                        如何培養下一代感知中華傳統文化之美？林屴汧表示，相信這些詩詞有其本身的價值，青年人到了一定的境界會明白的。當初成立合唱團，亦希望用容易接受的音樂去帶領青年人理解詩詞。音樂人在接受合唱團的訓練時，如何用音樂來表達歌詞的意思是十分講究的，因為它不同於器樂，器樂沒有歌詞，但是合唱是有歌詞的，因此必須要很深入地了解到每一個歌詞的意思。中國的字，一個字可以表達很多意思，一個字可以詩化了情景，尤其音樂其實是製造了一個意境出來，中國詩詞也一樣，兩者之間十分配合，兩種藝術雙璧合一。也可以看到，當團員一起訓練的時候，他們非常容易融入中國的詩詞。<br><br>
                                        <b>詩詞新唱 創作是一門藝術</b><br><br>
                                        「聲•華」詩詞音樂會中所有的歌曲都是以古詩詞或近代詩詞進行重新譜曲演繹的，林屴汧表示，中文歌和英文歌的創作不盡相同，中文是抑揚頓挫的，是用聲調表意的文字，但是英文是表音的文字，所有的歌詞都能配樂唱出來，中文的合唱的確比英文難。另外每一個聲部都唱不同的歌，有時候歌詞未必配合到音樂，比如現在合唱團有四個聲部，四個聲部都在唱不同的音去配合歌詞，只能通過作曲進行配合，一定要不斷地嘗試去找一條對的旋律，去配合到所有的歌詞，這個是在創作中遇到的最大的困難。<br><br>
                                        ＊＊＊ ＊＊＊ ＊＊＊<br><br>
                                        中國的文學極文化浪漫而源遠流長。目前華文社會都面對同樣的問題，即年輕人不了解詩詞的美麗。林屴汧寄語，盼望以音樂教育的全新模式，幫助年輕人了解中華傳統，將東方文學藝術之美傳承下去。<br><br>
                                        <b>Ponte Singers音樂人有話說</b><br><br>
                                        成立於2010年的香港合唱團Ponte Singers由本地青年音樂人組成，目前有約70名團員，他們有的是在讀的大學生，畢業於傳統中學；有的是剛剛踏入社會的青年人，身處各行各業。懷抱著對音樂的熱愛，使他們走到了一起。<br><br>
                                        Ponte 為意大利文，意即「橋」，表現了團員對音樂的熱情和執著，不單表現在音符之間，更希望能夠唱出聲音帶來的感覺，勾起聽眾不同經歷，建立另一種溝通的橋樑。<br><br>
                                        <b>沈韻琦 雙簧管樂手 作曲人</b><br>
                                        作為Ponte Singers的奠基人之一的沈韻琦從建團之初一直堅持至今，她不但作為樂手演奏雙簧管，還是樂團重要的作曲人之一。<br><br>
                                        「我覺得音樂帶給我的除了欣賞音樂的時候很舒服，或者演奏樂器的滿足感，或者和大家一起合奏的滿足感之外，在創作的過程裏面，扮演了很多新的角色。這首歌原本的感覺是這樣的，但我可以利用樂器編排，利用一些節奏，令到那首歌可以完全不同，令我個人可以大膽嘗試很多東西。我參與編曲的過程中和指揮很多時候會商討，當然我會用自己的方法編，但我都會想知道他可能希望今次的編排裏面，我們會一起合作做這件事。」<br><br>
                                        「我覺得最大的困難是歌唱究竟用粵語還是用國語，其實很多詩詞本身寫的時候都是近粵語的聲調，但是以唱歌來說，由於粵語真是那麼多聲調，其實是很難配合得好的。希望下次有機會嘗試粵語創作。」<br><br>
                                        <b>區婉坤 鋼琴樂手</b><br>
                                        作為Ponte Singers的奠基人之一的區婉坤從建團之初一直堅持至今，她一直是樂團重要的鋼琴手，幾乎每一次的音樂會都少不了她的倩影。<br><br>
                                        「我5歲開始學琴，從小學四年班開始彈choir，家人都支持。2010年加入Ponte Singers，一般在聖誕節和暑假參與活動，讀書忙之餘，在聖誕期間都是自己平衡這個時間，都不會影響讀書的。」<br><br>
                                        「有甚麼可以令到這個團可以繼續？就是音樂。除了跟觀眾之間通過音樂進行溝通外，團員之間也是一起唱一起玩，關係很好，音樂就是一個無形的橋。」<br><br>
                                        <b>蔣崇基 合唱團團員</b><br>
                                        畢業於皇仁書院的蔣崇基，自中學起就喜歡音樂，Ponte Singers陪伴了他整個大學時代。<br><br>
                                        「加入Ponte Singers很開心，能夠和不同的音樂人合唱，見到不同的香港新的、有才華、正在成長的音樂人，真的很開心。中學玩音樂，過了中學階段很少有機會有合唱一起唱，現在可以延續唱歌很幸運。」<br><br>
                                        <b>邱子健 鋼琴樂手 作曲人</b><br>
                                        本次「聲•華」詩詞合唱音樂會中唯一一首流行曲《銀髮白》，音樂人邱子健將該曲目重新創作，改編為八個聲部的合唱曲。剛剛大學畢業的他主修翻譯和哲學。<br><br>
                                        「音樂是一個表達自己的東西，平時我們用語言來溝通，很多時候我們覺得，我不會怎麼講啊，或者非筆墨可以形容的東西就可以用音樂表達出來。有時候音樂表達不到的用語言來表達。我覺得這個是相輔相成的。」<br><br>
