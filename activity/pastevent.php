                        <div class="caption">
                            讀古代文學有一個好處，就是可以看清楚一個民族的性格，知道這民族如何用文字，用藝術，去表達自己面對天地、面對生死的種種感受。<br><br>
                            不知道大家有否涉獵過中國的古文學，如果嫌詩經的感情太規矩，山海經又和生活有點距離，不妨看看楚國文學。大家寓居南方， 勉強來說對這南方的長江文學應該感受更深，亦只有南方那種離開封建，以自身的想像去理解天地的直接感觸，才能寫得出如「亦余心之所善兮，雖九死其猶未悔」般波瀾壯闊的胸襟，以至「心不同兮媒勞，恩不甚兮輕絕」的瀟洒達觀，將華文以圖為字，以詩為文的獨有特色發揮得淋漓盡致，這就是中國詩歌迷人之處。<br><br>
                            想當初不論是楚辭詩經，唐詩宋詞，以至元曲明劇，這些文字都是唱出來的，亦可以說全部都是當時的流行曲歌詞。六年前 Ponte Singers 將中國文學編輯成曲，蒐集不同以詩詞作曲的作品，一直努力的將舊詞配上新曲，將中詞配上西樂，抱有的只有一個信念，就是希望用大家現今接受的聲音和藝術，將中國古代的文學，以至音樂和整個文化觀，傳揚下去。<br><br>
                            今次的音樂會所選的歌曲，取材由宋詞到近代的新詩，當中亦包括本地流行曲，既然古代文學如是，近年文學獎亦包括歌詞，不妨一試。無論如何，中國的文字，中國的文學，甚至中國的文化，都是浪漫至極，想像豐富多彩的文學。難得大家學會了這世上最難學的語言文化，希望大家能好好欣賞、珍惜，並傳承中國的文學。<br><br>
