				<div id="2013" class="concert thumbnail" style="overflow: auto; margin: 10px;">
					<h3 style="font-weight: bold; margin-left: 20px;">2013 「生．息」音樂會</h3>
					<div id="left-col" class="col-md-5">
						<a href="/images/activity_pastevent_2013.jpg"><img src="/images/activity_pastevent_2013.jpg" alt="Ponte Singers Concert 2013" width="100%"></a>
					</div>
					<div id="right-col" class="col-md-7">
						<div class="caption">
							Ponte Singers 和 Orchestra 第四年度的音樂會，名為：「生．息」。今年 Ponte 選擇以德國音樂為主題，獻唱大型合唱及管弦樂作品，包括孟德爾遜的「芬加爾洞窟序曲」及奧爾夫的「布蘭詩歌」。 自中世紀開始，德語區的藝術對歐洲文化產生重大的影響，我們所熟知的西方音樂家，如巴哈、韓德爾、海頓、莫札特、貝多芬等等，皆於德奧兩地出生。<br><br>
							除了音樂以外，德國的文學、哲學等等在十七、十八世紀的歐洲亦獨領風騷。有異於法國和意大利等的文化區，德國人以嚴謹認真的態度，透過藝術和哲學，不斷探討人生、大自然和生活的種種命題，在音樂上，部分德國作曲家亦嘗試在曲式、主題和曲種等等尋求突破，以求更深入表達自己的人生觀。<br><br>
							今次音樂會以「芬加爾洞窟序曲」作為序曲，展現孟德爾遜如何以音樂表達自己對大自然的觀感，至於「布蘭詩歌」，則記載了當時中世紀的人生百態，以及人民對命運的想法。Ponte Singers 希望藉此兩首德國曲目，觀眾能夠窺探到德國文化藝術對大自然及人生種種命題的看法，讓觀眾對德國音樂的發展，特別是選擇音樂題材方面，有一個基本的概念。<br><br>
							Ponte Singers 今年邀請了200多名本地合唱界的青年人，以及海外及本地留學的一班音樂系畢業生組成的 Ponte Orchestra，一起參與演出；其中有首次參與大型合唱的中、小學生，亦有合唱經驗豐富的青年朋友。香港的青年合唱水平一向稱譽國際，Ponte Singers 僅希望藉此音樂會，為不同青年合唱團及大、中、小學的朋友提供一個藝術的交流平台，以舊帶新，傳承合唱的文化。<br><br>
