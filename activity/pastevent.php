                            今年是特別的禧年，教宗宣佈了主題，是「慈悲」。<br><br>
                            慈悲是人性之本，如果要歸根結底，慈悲是一種沒有緣由的感性，看到一些不幸的事、不幸的人，心底裏第一時間想幫助他們，同情他們。有時候，如果您理性點想多一點，您或許會停下來，想一想：他是不是騙子呢？我的慈悲有沒有被利用呢？於是，您便停下來了，有耐性的或許會看看再決定，不然便老早離去，多一事不如少一事。<br><br>
                            教宗特別宣揚慈悲，我想有他的意思。這個世代的文明走得太快太盡了，我們都忙於沿着一成不變的道理走下去，應該這樣的，不應那樣的，卻忘記了一份慈悲的本心；學習音樂也有這樣相似的體驗，我們很努力的學習曲譜上所記下的一切，卻忘記了藝術之所以感動，源於一種與人性相通的關係，只着眼於應該與不應該的記號，會失卻了當初喜愛音樂的本心。沉迷道理令人容易迷失；音樂也好，憐憫也好，要摒除一切直見人心，有時候亦需要一些經歷。<br><br>
                            慈悲也好，追尋人性的過程也好，都需要勇氣。Ponte Singers在六年前開始了它的音樂經歷，跌跌撞撞，一直扶持着的，就是尋找一種直見人心的感動，到最後意義是甚麼並不重要，最重要的，是彰顯了人性。<br><br>
                            馬勒《復活交響曲》裏的歌詞，也是挺有意思的：我們都是祂在人世間播下的種子，經歷種種風雨營役，在成熟的時候，祂便會收割下來，回到天上。<br><br>
