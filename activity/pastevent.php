					<div id="right-col" class="col-md-7">
						<div class="caption">						
							為什麼聽海？<br><br>
							友人戲稱，上網愛聽誰演奏都可以，為什麼要來音樂會。<br><br>
							的確，現場演出未必如錄音精準，即使是世界一流的大師和頂級的樂團，現場聽到的東西也不及細緻調校過的錄音：完美的音色、毫無瑕疵的起承轉合...以現在的錄音技術來說，一切都可以說是得心應手。<br><br>
							來聽現場音樂，您怕自己不是音樂的專家，聽不出誰走掉了音，或者哪一件樂器太嘈太霸道，你甚至未必有能耐去專注地聽畢一場音樂會，總在某些時候分了神，雙眼放了空，甚至不期然的睡過去了。於是，您說，自己不適合去音樂會。<br><br>
							但有一種感動，只有在音樂廳才會發生。<br><br>
							很多人以為音樂，是聲音的藝術，其實音樂最吸引的地方，在乎重新定義時間，短短一個多小時的音樂會，您坐在一個不能發聲的地方，外面的時間一分一秒的漏掉，卻被樂手以音樂的快慢，來重新定義您的時間。那是一個很浩大的計畫，那心計和精力，甚至令張愛玲也覺得陰謀重重令人吃怕。您的思緒於是一直的被帶動，心裡想的未必是音樂，可能是天馬行空的想像，一段久已遺忘的回憶，甚至是日常生活沒有膽量去想像的傻瓜事情，就是在這被音樂催動的而一直噤聲的場合，亦步亦趨的活潑起來。<br><br>
							音樂會，是一個想像、一個默想的空間，希望各位享受「聽．海」，在時間洪流中有一個愉快的旅程。<br><br>
