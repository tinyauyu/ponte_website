                    <h3 style="font-weight: bold; margin-left: 20px;">2017 「拉．柴」音樂會 </h3>
                    <div id="left-col" class="col-md-5">
                        <a href="/images/activity_pastevent_2017_orch.jpg"><img src="/images/activity_pastevent_2017_orch.jpg" alt="Ponte Orchestra Concert 2017" width="100%"></a>
