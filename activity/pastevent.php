					<h3 style="font-weight: bold; margin-left: 20px;">2015 「聽．海」音樂會</h3>
					<div id="left-col" class="col-md-5">
						<a href="/images/activity_pastevent_2015.jpg"><img src="/images/activity_pastevent_2015.jpg" alt="Ponte Singers Concert 2015" width="100%"></a>
