                            說世界上偉大的東西，源於浪漫，實不為過。<br><br>
                            浪漫，就是對自己喜愛的東西，抱有不切實際的幻想，人在少年時，很容易受喜愛的事物觸動，不論是鄰校的女同學、聽過的音樂、一個又一個故事裡的角色，還是各式各樣迷上的偶像，整個人像中了蠱毒一樣，不能自拔，腦海中浮現一個又一個沒有可能的情節，這幻想無限放大，真的可以去得很遠，可以說：情不知所起，一往而深。<br><br>
                            年青時的浪漫幻想，雖然一下子去得很遠，卻未必很深。談到浪漫，愛情故事的例子當然比比皆是，這裡想說的，卻是一個十分平庸的故事：曾經有一位中學同學，中一的時候聽到友人拉小提琴，一下子迷上了，努力練琴，短短幾年間從一個對音樂毫無認識和興趣的小子，成為了香港樂團的一個人物。後來上了大學，那般熱忱被種種的東西磨掉了，現在來當我們音樂會當聽眾。當然，世上總有偏執的年青人，他們一般被視為較「老實」的一群，看上了的東西便咬住不放，將十五歲在腦海中的幻想，默默的一步一步努力到老也不放棄。讀到這裡，畢業多年的您應該很快能下結論，說一句：對啊！這些人真的值得欣賞。然後再自反而「縮」，推搪一句「這世界哪來這麼多的XXX。」就這樣精明地審時度勢的一縮，縮渡一個愉快的人生，選擇如此，乃是緣份。<br><br>
                            浪漫是不切實際的，因為實際的東西世上已經存在了；不存在的，只能靠不切實際的幻想去完成。多年求學在外，悟到一個小小的道理，就是世上本無道理，循規蹈矩的向上學習，可以給您一個歡愉的人生，但世間的顛峰，往往不是過關般走上去的。藝術如是，科學如是，生財亦如是。人要浪漫，才有幻想，十多歲人要有幻想，幾十歲人更要有幻想，保有一顆不太理智的心，才能創造更多的東西。<br><br>
                            太上忘情，下不及情；情之所鍾，正在吾輩。希望大家別誤會，浪漫不是花心，一往而深，往往回吐更高。<br><br>
