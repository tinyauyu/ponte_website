                <p class="justify">
                    香港土生土長，被評為「組織能力及音樂處理成熟」（周凡夫先生）、「不能小覷的年輕力量」（洪思行先生）的 Ponte Singers 和 Ponte Orchestra （下稱 Ponte）是近年備受社會矚目的香港青年音樂組織，「Ponte」在意大利文中解作「橋」，象徵團員對音樂的追求，不單表現於音符之間，同時更希望築構心靈之間的橋樑，與聽眾更深層地交流應和。Ponte 匯聚了香港青年音樂界的菁英，成員為海外及本地音樂大學畢業生，立志延續他們在海外的見聞，為香港帶來不一樣的音樂體驗。
                </p>
                <p class="justify">
                    Ponte 成立於 2010 年 6 月，乃非牟利的青年合唱團和管弦樂團，多年來獲政府及各地邀請演出，包括德國柏林節、德國統一國慶日酒會、匈牙利合唱大師 Zoltán Pad 的指揮課示範合唱團、澳門青年音樂節、世界青年合唱節、香港天主教會等。同時，本團每年均舉辦主題式音樂會，嘉賓不乏本地政要及樂壇前輩，成為了本地樂迷每年引頸以待的盛事之一。
                </p>
                <p class="justify">
                    在過往多年的大型主題音樂會中，Ponte 的曲目皆精彩吸引，每次於本地大型場地演出，座無虛席：當中包括 2016 年「復．活」音樂會中的馬勒第二交響曲，首次於香港天主教聖母無原罪主教座堂演出，並於澳門玫瑰堂巡演； 2015 年「聽．海」音樂會中雷夫．佛漢．威廉士 (Ralph Vaughan Williams) 的《海洋交響曲》(A Sea Symphony)； 2014 年「錯．愛」音樂會中舒曼、妻子克拉拉及門徒布拉姆斯的作品； 2013 年卡爾．奧福 (Carl Orff) 的《布蘭詩歌》(Carmina Burana)，合唱團及管弦樂團往往合共 300 人演出，陣容及水平均媲美專業樂團，廣受樂界歡迎。
                </p>
                <p class="justify">
                    Ponte 亦積極和不同藝術媒體合作， 2014 年以「錯．愛」為主題，與電影攝製室 Timelogue 合作，結合電影及合唱音樂，突破傳統，反應熱烈。其他主題音樂會包括以宗教音樂為主題的「聖．靈」音樂會 (2010)、中國合唱音樂為題的「聲．華」音樂會 (2011)、以及以世界民族音樂為題的「凡．聲」音樂會 (2012)。這種主題式音樂會具教育意義，為聽眾對不同音樂形式留下深刻印象。同時Ponte亦參與不少亞洲電影的錄音製作，包括近年電影「危城」的錄音，以及與本港知名電影音樂創作人金培達先生、陳光榮先生及褚鎮東先生合作。此外，為了令分享音樂變得更有意義，每年我們均至四季酒店的慈善午宴以及各大小商場表演。本團多次被社會不同媒體訪問及報導，當中包括香港電台、《星島日報》、大學雜誌及本地藝術報刊等。
                </p>
                <p class="justify">
                    Ponte 堅持為香港的新進青年音樂家提供發展機會。每一年，我們均於音樂會保留兩至三首樂曲，予本地的青年作曲家及編曲家發表新作，並邀請新進樂手參與伴奏或獨奏。支持本地樂手之餘，我們亦希望聽眾對不同種類的音樂有另一番體會。因此，除了傳統中西曲目外，Ponte 亦會演繹其他不同曲風，如聖詩、中外流行曲、爵士樂、騷靈音樂、R&B 等，希望擴闊團員及聽眾對音樂的認知。
                </p>

